#include "Atom.h"
#include "Polygon.h"
#include "Game.h"

const Game::Polygon* Atom::defaultPolygon = nullptr;

Atom::Atom(coord2d* center, unsigned char number) : 
	Circle(defaultPolygon, center, &elements[number].second),
	number(number)
{
}

void Atom::draw()
{
	Circle::draw();
	const char* elem = elements[number].first;
	int a = strlen(elem);
	Game::write(elem, coord2d(center->x - strlen(elem) * 0.014f, center->y - 0.014f), { 1, 1, 1 });
}

void Atom::Init()
{
	Atom::defaultPolygon = new Game::Polygon(Circle::generateVertices(200, 0.1), 200);
}

